package com.example.dokkanseller.views.orders;

public class OrderItemModel {
    private int item_image;
    private String item_name;
    private String item_quantity;
    private String item_quantity_number;
    private int customer_image;
    private String customer_name;
    private String customer_address;
    private String customer_address_details;
    private String customer_phone;
    private String customer_phone_number;


    public OrderItemModel() {
    }

    public int getItem_image() {
        return item_image;
    }

    public void setItem_image(int item_image) {
        this.item_image = item_image;
    }

    public String getItem_name() {
        return item_name;
    }

    public void setItem_name(String item_name) {
        this.item_name = item_name;
    }

    public String getItem_quantity() {
        return item_quantity;
    }

    public void setItem_quantity(String item_quantity) {
        this.item_quantity = item_quantity;
    }

    public String getItem_quantity_number() {
        return item_quantity_number;
    }

    public void setItem_quantity_number(String item_quantity_number) {
        this.item_quantity_number = item_quantity_number;
    }

    public int getCustomer_image() {
        return customer_image;
    }

    public void setCustomer_image(int customer_image) {
        this.customer_image = customer_image;
    }

    public String getCustomer_name() {
        return customer_name;
    }

    public void setCustomer_name(String customer_name) {
        this.customer_name = customer_name;
    }

    public String getCustomer_address() {
        return customer_address;
    }

    public void setCustomer_address(String customer_address) {
        this.customer_address = customer_address;
    }

    public String getCustomer_address_details() {
        return customer_address_details;
    }

    public void setCustomer_address_details(String customer_address_details) {
        this.customer_address_details = customer_address_details;
    }

    public String getCustomer_phone() {
        return customer_phone;
    }

    public void setCustomer_phone(String customer_phone) {
        this.customer_phone = customer_phone;
    }

    public String getCustomer_phone_number() {
        return customer_phone_number;
    }

    public void setCustomer_phone_number(String customer_phone_number) {
        this.customer_phone_number = customer_phone_number;
    }


}
